begin
	Q=[1 2 3];
	for a=1:1000
    for b=a+1:a+5000
        c=a+b;
        X=factor(Vector,a*b*c);
        L=length(X);
        rad=X[1];
        if [gcd(a,b) gcd(a,c) gcd(b,c)]==[1 1 1]
            for i=2:L
                if X[i]==X[i-1]
                    rad=rad*1;
                else
                    rad=rad*X[i];
                end
            end
            if (c >= rad^κ)
                q=[a b c];
                Q=[Q;q];
            end
        end
    end
end

    abc=["a", "b", "c"]
    R=DataFrame(Q,Symbol.(abc));

end

